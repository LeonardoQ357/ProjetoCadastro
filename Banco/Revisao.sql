-- DML (Data Managment Language) => Comandos de manipulação de dados
-- EX: SELECT COLUNAS FROM TABELA
-- 	INSERT INTO TABELA (COLUNAS) VALUES (VALORES)
-- 	DELETE FROM TABELA
-- 	UPDATE COLUNAS FROM TABELA