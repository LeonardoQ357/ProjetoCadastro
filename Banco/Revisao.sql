-- DDL (Data Definition Language) => Comandos de definição do banco de dados
-- Ex: CREATE DATABASE NAME
-- 		DROP DATABASE NAME
-- 		CREATE TABLE NAME (COLUNAS)
-- 		DROP TABLE NAME

